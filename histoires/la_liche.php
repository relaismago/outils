	L'ordre arriva. Se rendre en un certain lieu, a une certaine heure, et attendre que s'ouvre le portail. Le coeur s'accélère, le sang commence a battre dans les tempes. Cette fois, c'est parti. Le portail passé, je restais la, bouche bée, ahuri par le nombre de trolls rassemblé. Et il en venait toujours plus, passant a travers le portail sans un mot. Alors commenca l'attente.<br><br>

	Rétrospectivement, c'est ce qui fut le plus dur. Attendre, la, bien plus bas que la plupart n'avait jamais osé descendre, bien trop bas pour beaucoup, dont moi. Halluciné, je me vis affronter des horreurs sans nom, d'une puissance suffisante pour me couper en deux d'un simple coup de griffe, me demandant a chaque instant si je verrai le terme du voyage. Enfin, il fut l'heure de passer le dernier portail, et d'affronter la Liche.<br><br>

